package flower;

public class SuperFlowers {

    private int quantity;
    private int price;

    public SuperFlowers(int quantity ) {
        this.quantity = quantity;

    }

    public int getQuantity() {
        return quantity;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
